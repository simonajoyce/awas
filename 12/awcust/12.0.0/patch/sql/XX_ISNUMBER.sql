  CREATE OR REPLACE FUNCTION "APPS"."XX_ISNUMBER" (x varchar2) RETURN NUMBER
AS

Y NUMBER;

BEGIN
  SELECT X*1 
  INTO Y
  FROM DUAL;
  
  RETURN 1 ;
  
EXCEPTION 
WHEN INVALID_NUMBER THEN
RETURN 0;
END XX_ISNUMBER;
 

/
